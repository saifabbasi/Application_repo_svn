	<h3 id="TrackingMethods">What is the difference between the three types of tracking methods (subID, pixel, postback)?</h3>
	<p>Often times, publishers may need to adjust their tracking methods based on the type of privileges they have with their offers or landing pages. Because of this, Bevo Media offers several different tracking methods to ensure we can provide a tracking solution for everyone, no matter what their situation is. The different types of tracking methods is as follows:</p>
	<ol>
		<li><strong>SubID Tracking:</strong> A Method of tracking in which the tracking information is passed via a unique number that is appended to the subIDs of an offer. This method requires nothing more than using a network with subID reporting. This is a highly used tracking method in search marketing.<li>
		<li><strong>Pixel Tracking:</strong> A method of tracking where a user places a 1x1 pixel on the conversion page of an offer. This pixel then fires a conversion to the Bevo interface, and the user is able to see the conversion detail. This method requires users to have the capability of placing a pixel on the conversion page of an offer. This is a very popular tracking method of media buy tracking.</li>
		<li><strong>Postback Tracking:</strong> A method of tracking in which the affiliate network pings Bevo every time a conversion has taken place. This method is limited because the affiliate network must have postback capabilities.</li>
	</ol>
	
