<h3 id="YSMText">The same as above, as a text tutorial:</h3>

<ol>
	<li>Log into your Yahoo Search Marketing account.</li>
	<li>Click the "Reports" tab</li>
	<li>Click the "Keyword Performance" link under the Reports Navigation (see <a href="#YSMScreenshot1">screenshot #1</a> below)</li>
	<li>Select a Specific date range. You must only select a SINGLE day. Yahoo's manual reporting system does not support multiple day break down in a single report. You must do one report per day (see <a href="#YSMScreenshot1">screenshot #1</a>).</li>
	<li>Click Download Report (see <a href="#YSMScreenshot1">screenshot #1</a>)</li>
	<li>Select XML Format</li>
	<li>Download your report</li>
	<li>Upload the "Keyword-Performance-Report" to the Bevo Media System for your designated Yahoo account. <b>Note:</b> Do not upload the "Performance-By-Date" report.</li>
</ol>
	
